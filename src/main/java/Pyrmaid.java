public class Pyrmaid {
    
     private String Pharaoh;
    private String AncientName;
    private String ModernName;
    private String Site;
    private double Height;

    /**
     * @return the Pharaoh
     */
    public String getPharaoh() {
        return Pharaoh;
    }

    /**
     * @param Pharaoh the Pharaoh to set
     */
    public void setPharaoh(String Pharaoh) {
        this.Pharaoh = Pharaoh;
    }

    /**
     * @return the AncientName
     */
    public String getAncientName() {
        return AncientName;
    }

    /**
     * @param AncientName the AncientName to set
     */
    public void setAncientName(String AncientName) {
        this.AncientName = AncientName;
    }

    /**
     * @return the ModernName
     */
    public String getModernName() {
        return ModernName;
    }

    /**
     * @param ModernName the ModernName to set
     */
    public void setModernName(String ModernName) {
        this.ModernName = ModernName;
    }

    /**
     * @return the Site
     */
    public String getSite() {
        return Site;
    }

    /**
     * @param Site the Site to set
     */
    public void setSite(String Site) {
        this.Site = Site;
    }

    /**
     * @return the Height
     */
    public Double getHeight() {
        return Height;
    }

    /**
     * @param Height the Height to set
     */
    public void setHeight(Double Height) {
        this.Height = Height;
    }
  
   

}
